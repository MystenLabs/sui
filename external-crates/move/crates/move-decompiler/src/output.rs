use move_model::symbol::Symbol;
use move_model_2::{
    model::{self as M, Model},
    pretty::ToDoc,
    source_kind::SourceKind,
};

use pretty::RcDoc;

use std::path::Path;

pub fn generate_output<S: SourceKind>(input: Model<S>, output: &Path) -> anyhow::Result<()> {
    let decompiled = crate::translate::model(input)?;

    let crate::ast::Decompiled { model, packages } = decompiled;

    println!("model packages");
    println!(
        "{:#?}",
        model
            .packages()
            .map(|p| (p.name(), p.address()))
            .collect::<Vec<_>>()
    );
    println!("GENERATING OUTPUT");

    for pkg in packages {
        let name = pkg
            .name
            .map(|name| name.as_str().to_owned())
            .unwrap_or_else(|| format!("anon_{}", pkg.address));

        println!("package: {} @ {}", name, pkg.address);
        // Ensure the package directory exists and is empty: output/pkg_name
        let pkg_dir = output.join(&name);
        std::fs::create_dir_all(&pkg_dir)?;
        std::fs::remove_dir(&pkg_dir)?;
        std::fs::create_dir_all(&pkg_dir)?;
        println!("dir: {}", pkg_dir.display());

        let Some(model_pkg) = model.maybe_package(&pkg.address) else {
            anyhow::bail!("Package with address {} not found in model", pkg.address);
        };

        // Iterate without moving the map/vec
        for (module_name, module) in &pkg.modules {
            let path = pkg_dir.join(format!("{module_name}.move"));
            // If generate_output returns a Result, use `?`; otherwise drop it
            generate_module(model_pkg, &path, &name, module)?;
        }
    }

    Ok(())
}

fn generate_module<S: SourceKind>(
    pkg: M::Package<'_, S>,
    path: &Path,
    pkg_name: &str,
    module: &crate::ast::Module,
) -> anyhow::Result<()> {
    use RcDoc as RD;

    let Some(model_mod) = pkg.maybe_module(module.name) else {
        anyhow::bail!("Module {} not found in package {}", module.name, pkg_name);
    };

    let crate::ast::Module { name, functions } = module;

    let mut doc = RcDoc::<'static, ()>::text("// Auto-generated by Move decompiler")
        .append(RcDoc::hardline())
        .append(RcDoc::hardline());

    doc = doc
        .append(RcDoc::text("module"))
        .append(RcDoc::space())
        .append(RcDoc::as_string(pkg_name))
        .append(RcDoc::text("::"))
        .append(RcDoc::as_string(name))
        .append(RcDoc::text(";"))
        .append(RcDoc::hardline())
        .append(RcDoc::hardline());

    doc = doc
        .append(RD::text("// -- structs -- "))
        .append(RD::hardline())
        .append(RD::hardline());

    let structs: RcDoc<'static, ()> = {
        let mut doc: RcDoc<'static, ()> = RD::nil();
        for s in model_mod.structs() {
            let s_doc: RcDoc<'static, ()> = s.to_doc();
            doc = doc
                .append(s_doc)
                .append(RD::hardline())
                .append(RD::hardline());
        }
        doc
    };
    doc = doc.append(structs);

    doc = doc
        .append(RD::text("// -- enums -- "))
        .append(RD::hardline())
        .append(RD::hardline());

    let enums: RcDoc<'static, ()> = {
        let mut doc: RcDoc<'static, ()> = RD::nil();
        for e in model_mod.enums() {
            let e_doc: RcDoc<'static, ()> = e.to_doc();
            doc = doc
                .append(e_doc)
                .append(RD::hardline())
                .append(RD::hardline());
        }
        doc
    };
    doc = doc.append(enums);

    doc = doc
        .append(RD::text("// -- functions -- "))
        .append(RD::hardline())
        .append(RD::hardline());

    let functions: RD<'static, ()> = {
        let mut doc: RD<'static, ()> = RD::nil();
        for (name, fun) in functions {
            let Some(model_fun) = model_mod.maybe_function(*name) else {
                anyhow::bail!("Function {} not found in module {}", name, module.name);
            };
            let f_doc: RcDoc<'static, ()> = generate_function(&model_fun, fun);
            doc = doc
                .append(f_doc)
                .append(RD::hardline())
                .append(RD::hardline());
        }
        doc
    };
    doc = doc.append(functions);

    let mut w = Vec::new();
    doc.render(100, &mut w).unwrap();
    let output = String::from_utf8(w).unwrap();
    std::fs::write(path, output)?;
    Ok(())
}

fn generate_function<S: SourceKind>(
    model_fun: &M::Function<'_, S>,
    fun: &crate::ast::Function,
) -> RcDoc<'static, ()> {
    // TODO: Docs, Attributes
    let header = move_model_2::pretty::fun_header(model_fun);

    let crate::ast::Function { name: _, code } = fun;
    let exp_doc = crate::pretty::to_doc(code);

    header
        .append(RcDoc::space())
        .append(RcDoc::text("{"))
        .append(RcDoc::nest(RcDoc::hardline().append(exp_doc), 4))
        .append(RcDoc::hardline())
        .append(RcDoc::text("}"))
}
