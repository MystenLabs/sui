pub fn module<S: SourceKind>(
    model: &model::Model<S>,
    pkg_name: &str,
    model_mod: model::Module<'_, S>,
    module: &ast::Module,
) -> anyhow::Result<Doc> {
    // We have no good way to identify constants: in the model, they return the information held in
    // their definition RC, plus their module and actual bytes. We need a way to compare these to
    // the constants we find in the bytecode so we can link them up.
    // We do have one option: we can write down the actual in-memory pointer of the constant
    // definition RC, and use that as a key. This is not ideal, but it should be stable enough for
    // our purposes here.
    let constant_table: IndexMap<
        *const move_model_2::normalized::Constant,
        (String, model::CompiledConstant<S>),
    > = model_mod
        .compiled_constants()
        .enumerate()
        .map(|(ndx, c)| (c.compiled() as *const _, (format!("C{ndx}"), c)))
        .collect();

    let context = {
        let constant_table: IndexMap<*const Constant<Symbol>, String> = constant_table
            .iter()
            .map(|(k, (name, _))| (*k, name.clone()))
            .collect();

        Context { constant_table }
    };

    let crate::ast::Module {
        name,
        structs,
        enums,
        functions,
        uses,
        type_uses,
    } = module;

    let mut doc = D::text("// Auto-generated by Move decompiler")
        .concat(D::line())
        .concat(D::line());

    doc = doc
        .concat(D::text("module"))
        .concat_space(D::text(pkg_name))
        .concat(D::text("::"))
        .concat(D::text(name.as_str()))
        .concat(D::text(";"))
        .concat(D::line())
        .concat(D::line());

    if !uses.is_empty() || !type_uses.is_empty() {
        doc = doc
            .concat(D::text("// -- uses -- "))
            .concat(D::line())
            .concat(D::line());
        for (mid, alias) in uses {
            let line = if *alias == mid.name {
                format!("use {mid};")
            } else {
                format!("use {mid} as {alias};")
            };
            doc = doc.concat(D::text(line)).concat(D::line());
        }
        for ((mid, type_name), alias) in type_uses {
            let line = if alias == type_name {
                format!("use {mid}::{type_name};")
            } else {
                format!("use {mid}::{type_name} as {alias};")
            };
            doc = doc.concat(D::text(line)).concat(D::line());
        }
        doc = doc.concat(D::line());
    }

    if !structs.is_empty() {
        doc = doc
            .concat(D::text("// -- structs -- "))
            .concat(D::line())
            .concat(D::line());

        let structs_doc = {
            let mut doc = D::nil();
            for s in structs.values() {
                doc = doc
                    .concat(struct_doc(s))
                    .concat(D::line())
                    .concat(D::line());
            }
            doc
        };
        doc = doc.concat(structs_doc);
    }

    if !enums.is_empty() {
        doc = doc
            .concat(D::text("// -- enums -- "))
            .concat(D::line())
            .concat(D::line());

        let enums_doc = {
            let mut doc = D::nil();
            for e in enums.values() {
                doc = doc.concat(enum_doc(e)).concat(D::line()).concat(D::line());
            }
            doc
        };
        doc = doc.concat(enums_doc);
    }

    if !constant_table.is_empty() {
        doc = doc
            .concat(D::text("// -- constants -- "))
            .concat(D::line())
            .concat(D::line());

        let constants = {
            let summary_context = summary::Context::new(model);

            let mut doc = D::nil();
            for (_, (name, constant)) in constant_table.iter() {
                let const_ = constant.compiled();
                let c_doc = D::text(format!("const {name}"))
                    .concat(D::text(":"))
                    .concat_space(
                        summary::Type::from_normalized(&summary_context, &const_.type_).to_doc(),
                    )
                    .group()
                    .concat_space(D::text("="))
                    .concat_space(value(constant.value()))
                    .concat(D::text(";"));
                doc = doc.concat(c_doc).concat(D::line()).concat(D::line());
            }
            doc
        };
        doc = doc.concat(constants);
    }

    if !functions.is_empty() {
        doc = doc
            .concat(D::text("// -- functions -- "))
            .concat(D::line())
            .concat(D::line());

        let functions_doc = {
            let mut doc = D::nil();
            for fun in functions.values() {
                let f_doc = function(&context, fun);
                doc = doc.concat(f_doc).concat(D::line()).concat(D::line());
            }
            doc
        };
        doc = doc.concat(functions_doc);
    }

    Ok(doc)
}

fn function(context: &Context, fun: &Function) -> Doc {
    let header = fun_header_doc(fun);
    let code = &fun.code;

    // Three cases for the body to avoid double-bracing:
    // - empty Seq (e.g., from a refined-away trailing `return`) -> just `{}`
    // - non-empty Seq -> already renders as `{ ... }`, use it as-is
    // - any other Exp -> wrap in our own braces
    match code {
        Exp::Seq(seq) if seq.is_empty() => header.concat_space(Doc::text("{}")),
        Exp::Seq(_) => header.concat_space(exp(context, code)),
        _ => header
            .concat_space(Doc::text("{"))
            .concat(Doc::nest(Doc::line().concat(exp(context, code)), 4))
            .concat(Doc::line())
            .concat(Doc::text("}")),
    }
}

// -------------------------------------------------------------------------------------------------
// Header / type renderers (AST-driven, alias-aware)
//
// Anything that prints a `Type` ultimately bottoms out at a `TypeRef`'s `Display` impl, which
// already knows about `Aliased` vs. `Qualified`. The renderers below just walk the AST shapes
// in the same shape as `move_model_2::pretty_printer::fun_header` etc. — the difference is the
// types they read have been through `collect_uses`.

fn fun_header_doc(fun: &Function) -> Doc {
    let Function {
        name,
        visibility,
        is_entry,
        type_parameters,
        parameters,
        returns,
        code: _,
    } = fun;

    let mut prefix_parts: Vec<Doc> = Vec::new();
    if let Some(v) = visibility_doc(*visibility) {
        prefix_parts.push(v);
    }
    if *is_entry {
        prefix_parts.push(D::text("entry"));
    }
    let prefix = if prefix_parts.is_empty() {
        D::nil()
    } else {
        D::intersperse(prefix_parts, D::space()).concat(D::space())
    };

    let name_doc = D::text("fun").concat_space(D::text(name.as_str()));

    let tparams_doc = if type_parameters.is_empty() {
        D::nil()
    } else {
        // Function type parameters are abilities-only in bytecode (no phantom marker on
        // function tparams). Render as `T{i}` with optional `: abilities` constraints.
        D::angles(
            D::intersperse(
                type_parameters
                    .iter()
                    .enumerate()
                    .map(|(i, abilities)| fun_tparam_doc(i, *abilities)),
                D::text(",").concat(D::space()),
            )
            .group(),
        )
    };

    let params_doc = {
        // Parameter names follow the same `l{i}` scheme as `term_reconstruction::local_name`.
        let parts = parameters.iter().enumerate().map(|(i, ty)| {
            D::text(format!("l{i}"))
                .concat(D::text(":"))
                .group()
                .concat_space(type_doc(ty))
                .group()
        });
        D::intersperse(parts, D::text(",").concat(D::space()))
            .group()
            .parens()
    };

    let ret_doc = returns_doc(returns);

    prefix
        .concat(name_doc)
        .concat(tparams_doc)
        .concat(params_doc)
        .concat(ret_doc)
        .group()
}

fn returns_doc(rets: &[Type]) -> Doc {
    match rets {
        [] => D::nil(),
        [t] => D::text(": ").concat(type_doc(t)),
        many => {
            let items = many.iter().map(type_doc);
            D::text(": ").concat(
                D::softline()
                    .concat(D::intersperse(items, D::text(",").concat(D::softline())))
                    .nest(4)
                    .parens()
                    .group(),
            )
        }
    }
}

fn visibility_doc(v: Visibility) -> Option<Doc> {
    match v {
        Visibility::Private => None,
        Visibility::Public => Some(D::text("public")),
        Visibility::Friend => Some(D::text("public(friend)")),
    }
}

fn fun_tparam_doc(idx: usize, constraints: AbilitySet) -> Doc {
    let name = D::text(format!("T{idx}"));
    if let Some(c) = ability_constraints_doc(constraints) {
        name.concat(D::text(":")).concat_space(c).group()
    } else {
        name
    }
}

fn datatype_tparam_doc(idx: usize, tp: &DatatypeTyParameter) -> Doc {
    let phantom = if tp.is_phantom {
        D::text("phantom").concat(D::space())
    } else {
        D::nil()
    };
    let name = phantom.concat(D::text(format!("T{idx}")));
    if let Some(c) = ability_constraints_doc(tp.constraints) {
        name.concat(D::text(":")).concat_space(c).group()
    } else {
        name
    }
}

/// Render the abilities in an `AbilitySet`, in the same order as
/// `move_model_2::summary::Ability` (copy, drop, key, store) so output stays stable against
/// any upstream changes to AbilitySet iteration. `sep` is the joiner — `+` for constraints
/// in type-parameter positions, `,` for struct/enum `has` clauses.
fn ability_parts(s: AbilitySet) -> Vec<Doc> {
    let mut parts: Vec<Doc> = Vec::new();
    if s.has_copy() {
        parts.push(D::text("copy"));
    }
    if s.has_drop() {
        parts.push(D::text("drop"));
    }
    if s.has_key() {
        parts.push(D::text("key"));
    }
    if s.has_store() {
        parts.push(D::text("store"));
    }
    parts
}

/// `copy + drop + key + store` rendering for type-parameter constraints. Returns `None` when
/// the set is empty so callers can elide the `: ` separator entirely.
fn ability_constraints_doc(s: AbilitySet) -> Option<Doc> {
    let parts = ability_parts(s);
    if parts.is_empty() {
        None
    } else {
        Some(D::intersperse(parts, D::text(" + ")).group())
    }
}

/// `has copy, drop` ability list for struct/enum declarations — same set as
/// `ability_constraints_doc` but comma-separated instead of `+`-separated.
fn ability_list_doc(s: AbilitySet) -> Option<Doc> {
    let parts = ability_parts(s);
    if parts.is_empty() {
        None
    } else {
        Some(D::intersperse(parts, D::text(",").concat(D::space())).group())
    }
}

fn struct_doc(s: &Struct) -> Doc {
    let Struct {
        name,
        abilities,
        type_parameters,
        fields,
    } = s;

    let tparams = if type_parameters.is_empty() {
        D::nil()
    } else {
        D::angles(
            D::intersperse(
                type_parameters
                    .iter()
                    .enumerate()
                    .map(|(i, tp)| datatype_tparam_doc(i, tp)),
                D::text(",").concat(D::space()),
            )
            .group(),
        )
    };
    let abilities_doc = ability_list_doc(*abilities)
        .map(|c| D::softline().concat(D::text("has").concat_space(c)).group())
        .unwrap_or(D::nil());

    D::text("public struct")
        .concat_space(D::text(name.as_str()))
        .concat(tparams)
        .group()
        .concat(abilities_doc)
        .concat_space(fields_doc(fields))
        .group()
}

fn enum_doc(e: &Enum) -> Doc {
    let Enum {
        name,
        abilities,
        type_parameters,
        variants,
    } = e;

    let tparams = if type_parameters.is_empty() {
        D::nil()
    } else {
        D::angles(
            D::intersperse(
                type_parameters
                    .iter()
                    .enumerate()
                    .map(|(i, tp)| datatype_tparam_doc(i, tp)),
                D::text(",").concat(D::space()),
            )
            .group(),
        )
    };
    let abilities_doc = ability_list_doc(*abilities)
        .map(|c| D::softline().concat(D::text("has").concat_space(c)).group())
        .unwrap_or(D::nil());

    let variants_doc = D::intersperse(
        variants.iter().map(|v| variant_doc(v).concat(D::text(","))),
        D::line(),
    );

    D::text("public enum")
        .concat_space(D::text(name.as_str()))
        .concat(tparams)
        .group()
        .concat(abilities_doc)
        .concat_space(
            D::line()
                .concat(variants_doc.indent(4))
                .concat(D::line())
                .braces(),
        )
}

fn variant_doc(v: &Variant) -> Doc {
    let Variant { name, fields } = v;
    if fields.is_empty() {
        D::text(name.as_str())
    } else {
        D::text(name.as_str()).concat_space(fields_doc(fields))
    }
}

/// `{ name: ty, ... }` — used for both struct fields and enum variant fields. Empty fields
/// render as `{}`. Wide-vs.-tall layout mirrors model-2's `Fields::to_doc` so output stays
/// stable: the wide form is `{ a: A, b: B }`, the tall form has each field on its own line
/// with a trailing comma.
fn fields_doc(fields: &[(Symbol, Type)]) -> Doc {
    if fields.is_empty() {
        return D::nil().braces();
    }
    let items: Vec<Doc> = fields
        .iter()
        .map(|(name, ty)| {
            D::text(name.as_str())
                .concat(D::text(":"))
                .concat_space(type_doc(ty))
        })
        .collect();

    let wide = D::space().concat(
        D::intersperse(items.iter().cloned(), D::text(",").concat(D::space())).concat(D::space()),
    );
    let tall = D::line()
        .concat(
            D::intersperse(items, D::text(",").concat(D::line()))
                .concat(D::text(","))
                .indent(4),
        )
        .concat(D::line())
        .group();
    D::alt(wide, tall).group().braces()
}

fn type_doc(t: &Type) -> Doc {
    match t {
        Type::Bool => D::text("bool"),
        Type::U8 => D::text("u8"),
        Type::U16 => D::text("u16"),
        Type::U32 => D::text("u32"),
        Type::U64 => D::text("u64"),
        Type::U128 => D::text("u128"),
        Type::U256 => D::text("u256"),
        Type::Address => D::text("address"),
        Type::Signer => D::text("signer"),
        Type::Vector(inner) => D::text("vector").concat(D::angles(type_doc(inner))).group(),
        Type::Reference(is_mut, inner) => {
            let head = if *is_mut {
                D::text("&mut").concat(D::space())
            } else {
                D::text("&")
            };
            head.concat(type_doc(inner)).group()
        }
        Type::TypeParameter(idx) => D::text(format!("T{idx}")),
        Type::Datatype(dt) => datatype_doc(dt),
    }
}

fn datatype_doc(dt: &Datatype) -> Doc {
    let Datatype {
        type_ref,
        type_arguments,
    } = dt;
    let head = D::text(format!("{type_ref}"));
    if type_arguments.is_empty() {
        head
    } else {
        head.concat(D::angles(
            D::intersperse(
                type_arguments.iter().map(type_doc),
                D::text(",").concat(D::space()),
            )
            .group(),
        ))
        .group()
    }
}

fn exp(context: &Context, exp: &Exp) -> Doc {
    fn braces_block(body: Doc) -> Doc {
        D::braces(D::line().concat(body.indent(4)).concat(D::line()))
    }

    // Render a list of statements separated by lines.
    fn stmts<'a, I>(context: &Context, it: I) -> Doc
    where
        I: IntoIterator<Item = &'a Exp>,
    {
        D::intersperse(
            it.into_iter().map(|e| recur(context, e)),
            D::text(";").concat(D::line()),
        )
    }

    // Expression-ish printers --------------------------------------------

    fn recur(context: &Context, e: &Exp) -> Doc {
        match e {
            Exp::Break(label) => match label {
                Some(l) => D::text(format!("break 'loop_{}", l)),
                None => D::text("break"),
            },
            Exp::Continue(label) => match label {
                Some(l) => D::text(format!("continue 'loop_{}", l)),
                None => D::text("continue"),
            },
            Exp::Return(es) => match &es[..] {
                [] => D::text("return"),
                [x] => D::text("return").concat_space(recur(context, x)),
                _ => D::text("return").concat_space(exp_list(context, es).parens()),
            },
            Exp::Assign(lhs, rhs) => match &lhs[..] {
                [] => recur(context, rhs),
                [x] => D::text(x)
                    .concat_space(D::text("="))
                    .concat_space(recur(context, rhs)),
                _ => {
                    let lhs =
                        D::intersperse(lhs.iter().map(D::text), D::text(",").concat(D::space()))
                            .parens()
                            .group();
                    lhs.concat_space(D::text("="))
                        .concat_space(recur(context, rhs))
                }
            },
            Exp::LetBind(lhs, rhs) => {
                let lhs_doc = match &lhs[..] {
                    [] => D::text("_"),
                    [x] => D::text(x),
                    _ => D::parens(D::intersperse(
                        lhs.iter().map(D::text),
                        D::text(",").concat(D::space()),
                    ))
                    .group(),
                };
                D::text("let")
                    .concat_space(lhs_doc)
                    .concat_space(D::text("="))
                    .concat_space(recur(context, rhs))
            }
            Exp::Declare(lhs) => {
                let lhs_doc = match &lhs[..] {
                    [] => D::text("_"),
                    [x] => D::text(x),
                    _ => D::parens(D::intersperse(
                        lhs.iter().map(D::text),
                        D::text(",").concat(D::space()),
                    ))
                    .group(),
                };
                D::text("let").concat_space(lhs_doc)
            }
            Exp::Call((m, f), args) => {
                let head = if m.is_builtin() {
                    D::text(format!("{f}"))
                } else {
                    D::text(format!("{m}::{f}"))
                };
                head.concat(exp_list(context, args).parens())
            }
            Exp::Abort(e) => D::text("abort").concat_space(recur(context, e)),
            Exp::Borrow(mutable, e) => {
                if *mutable {
                    D::text("&mut").concat_space(recur(context, e))
                } else {
                    D::text("&").concat(recur(context, e))
                }
            }
            Exp::Value(v) => value(v),
            Exp::Variable(s) => D::text(s),
            Exp::Constant(c) => context.get_constant(c),
            Exp::Seq(vs) => {
                let final_semi = matches!(
                    vs.last(),
                    Some(Exp::LetBind(_, _) | Exp::Assign(_, _) | Exp::Declare(_))
                );
                let mut stmts = stmts(context, vs);
                if final_semi {
                    stmts = stmts.concat(D::text(";"));
                }
                braces_block(stmts)
            }
            Exp::Loop(label, b) => {
                let header = match label {
                    Some(l) => D::text(format!("'loop_{}: loop", l)),
                    None => D::text("loop"),
                };
                header.concat_space(e_block(context, b))
            }
            Exp::While(label, c, b) => while_doc(context, *label, c, b),
            Exp::IfElse(c, t, e) => if_doc(context, c, t, e),
            Exp::Switch(subject, enum_ty, arms) => {
                let arms_doc = Doc::intersperse(
                    arms.iter().map(|(variant, body)| {
                        D::text(format!("{enum_ty}::{variant}"))
                            .concat_space(D::text("=>"))
                            .concat_space(e_block(context, body))
                    }),
                    D::text(",").concat(D::line()),
                );
                D::text("match")
                    .concat_space(D::parens(recur(context, subject)))
                    .concat_space(braces_block(arms_doc))
            }
            Exp::Match(subject, _enum_ty, arms) => {
                let arms_doc = Doc::intersperse(
                    arms.iter().map(|(variant, fields, body)| {
                        let mut pat = D::text(variant.as_str());
                        if !fields.is_empty() {
                            let field_doc = Doc::intersperse(
                                fields
                                    .iter()
                                    .map(|(sym, name)| D::text(format!("{sym}: {name}"))),
                                D::text(", "),
                            );
                            pat = pat
                                .concat_space(D::text("{"))
                                .concat_space(field_doc)
                                .concat_space(D::text("}"));
                        }
                        pat.concat_space(D::text("=>"))
                            .concat_space(e_block(context, body))
                    }),
                    D::text(",").concat(D::line()),
                );
                D::text("match")
                    .concat_space(D::parens(recur(context, subject)))
                    .concat_space(braces_block(arms_doc))
            }
            Exp::Primitive { op, args } => primitive_op_doc(context, op, args),
            Exp::Data { op, args } => data_op_doc(context, op, args),
            Exp::Unpack(struct_ty, items, exp) => {
                let items_doc = fields(items);
                D::text("let")
                    .concat_space(D::text(format!("{struct_ty}")))
                    .concat_space(items_doc)
                    .concat_space(D::text("="))
                    .concat_space(recur(context, exp))
            }
            Exp::VecUnpack(lhs, exp) => {
                if lhs.is_empty() {
                    D::text("std::vector::destroy_empty").concat(recur(context, exp).parens())
                } else {
                    D::text("/* UNSUPPORT OP: MULTIARG VEC UNPACK ON ")
                        .concat(recur(context, exp))
                        .concat(D::text(" */"))
                }
            }
            Exp::UnpackVariant(unpack_kind, (enum_ty, variant), items, exp) => {
                let items_doc = fields(items);
                let rhs_prefix = match unpack_kind {
                    ast::UnpackKind::Value => "",
                    ast::UnpackKind::ImmRef => "&",
                    ast::UnpackKind::MutRef => "&mut ",
                };
                let rhs = D::text(rhs_prefix).concat(recur(context, exp));
                D::text("let")
                    .concat_space(D::text(format!("{enum_ty}::{variant}")))
                    .concat_space(items_doc)
                    .concat_space(D::text("="))
                    .concat_space(rhs)
            }
            Exp::Unstructured(nodes) => {
                D::text("unstructured").concat_space(unstructured_block(context, nodes))
            }
        }
    }

    fn e_block(context: &Context, e: &Exp) -> Doc {
        // If it's already a block/seq, print its statements;
        // otherwise, treat the single expression as a statement.
        match e {
            Exp::Seq(vs) => {
                let final_semi = matches!(
                    vs.last(),
                    Some(Exp::LetBind(_, _) | Exp::Assign(_, _) | Exp::Declare(_))
                );
                let mut stmts = stmts(context, vs);
                if final_semi {
                    stmts = stmts.concat(D::text(";"));
                }
                braces_block(stmts)
            }
            other => {
                let final_semi = matches!(
                    other,
                    Exp::LetBind(_, _) | Exp::Assign(_, _) | Exp::Declare(_)
                );
                let mut body = recur(context, other);
                if final_semi {
                    body = body.concat(D::text(";"));
                }
                braces_block(body)
            }
        }
    }

    fn unstructured_block(context: &Context, nodes: &[ast::UnstructuredNode]) -> Doc {
        let nodes_doc = D::intersperse(
            nodes.iter().map(|node| unstructured_node(context, node)),
            D::line(),
        );
        braces_block(nodes_doc)
    }

    fn unstructured_node(context: &Context, node: &ast::UnstructuredNode) -> Doc {
        match node {
            ast::UnstructuredNode::Labeled(label, body) => {
                D::text(format!("'label_{}:", label)).concat_space(e_block(context, body))
            }
            ast::UnstructuredNode::Statement(exp) => recur(context, exp),
            ast::UnstructuredNode::Goto(label) => D::text(format!("goto 'label_{};", label)),
        }
    }

    fn while_doc(context: &Context, label: Option<ast::Label>, cond: &Exp, body: &Exp) -> Doc {
        let header = match label {
            Some(l) => D::text(format!("'loop_{}: while", l)),
            None => D::text("while"),
        };
        header
            .concat_space(recur(context, cond).parens())
            .concat_space(e_block(context, body))
    }

    fn if_doc(context: &Context, cond: &Exp, then_b: &Exp, else_b: &Option<Exp>) -> Doc {
        let then_block = e_block(context, then_b);
        match else_b {
            None => D::text("if")
                .concat_space(recur(context, cond).parens())
                .concat_space(then_block),
            Some(e) => {
                let else_block = e_block(context, e);
                D::text("if")
                    .concat_space(recur(context, cond).parens())
                    .concat_space(then_block)
                    .concat_space(D::text("else"))
                    .concat_space(else_block)
            }
        }
    }
    recur(context, exp)
}

/// Render a list of expressions separated by commas.
fn exp_list<'a, I>(context: &Context, it: I) -> Doc
where
    I: IntoIterator<Item = &'a Exp>,
{
    D::intersperse(
        it.into_iter().map(|e| exp(context, e)),
        D::text(",").concat(D::space()),
    )
}

/// Render a list of fields (name, type) separated by commas and enclosed in braces.
fn fields(fields: &[(Symbol, String)]) -> Doc {
    if fields.is_empty() {
        return D::nil().braces();
    };
    let doc = D::intersperse(
        fields.iter().map(|(name, ty)| {
            D::text(name.as_str())
                .concat(D::text(":"))
                .concat_space(D::text(ty))
        }),
        D::text(",").concat(D::space()),
    );
    D::space().concat(doc).concat(D::space()).braces()
}

fn data_op_doc(context: &Context, op: &DataOp, args: &[Exp]) -> Doc {
    fn maybe_parens(context: &Context, e: &Exp) -> Doc {
        match e {
            Exp::Variable(_) | Exp::Value(_) | Exp::Constant(_) => exp(context, e),
            _ => exp(context, e).parens(),
        }
    }

    match op {
        DataOp::ReadRef => D::text("*").concat(maybe_parens(context, &args[0])),

        DataOp::WriteRef => D::text("*")
            .concat(maybe_parens(context, &args[0]))
            .concat_space(D::text("="))
            .concat_space(exp(context, &args[1])),
        DataOp::FreezeRef => D::text("freeze").concat(exp(context, &args[0]).parens()),

        DataOp::MutBorrowField(field_ref) => D::text("&mut ")
            .concat(maybe_parens(context, &args[0]))
            .concat(D::text("."))
            .concat(D::text(field_ref.field.name.as_str())),

        DataOp::ImmBorrowField(field_ref) => D::text("&")
            .concat(maybe_parens(context, &args[0]))
            .concat(D::text("."))
            .concat(D::text(field_ref.field.name.as_str())),

        DataOp::VecPack(_) => D::text("vector[")
            .concat(exp_list(context, args))
            .concat(D::text("]")),

        DataOp::VecLen(_) => exp(context, &args[0]).concat(D::text(".len()")),

        DataOp::VecImmBorrow(_) => D::text("&")
            .concat(maybe_parens(context, &args[0]))
            .concat(D::text("["))
            .concat(exp(context, &args[1]))
            .concat(D::text("]")),

        DataOp::VecMutBorrow(_) => D::text("&mut ")
            .concat(maybe_parens(context, &args[0]))
            .concat(D::text("["))
            .concat(exp(context, &args[1]))
            .concat(D::text("]")),

        DataOp::VecPushBack(_) => maybe_parens(context, &args[0])
            .concat(D::text(".push_back("))
            .concat(exp(context, &args[1]))
            .concat(D::text(")")),

        DataOp::VecPopBack(_) => maybe_parens(context, &args[0]).concat(D::text(".pop_back()")),

        DataOp::VecSwap(_) => maybe_parens(context, &args[0])
            .concat(D::text(".swap("))
            .concat(exp(context, &args[1]))
            .concat(D::text(", "))
            .concat(exp(context, &args[2]))
            .concat(D::text(")")),

        DataOp::PackVariant(variant) => {
            let fields = &variant.variant.fields.0;
            assert!(fields.len() == args.len());
            let enum_name = variant.enum_.name;
            let variant_name = variant.variant.name;
            D::text(format!("{enum_name}::{variant_name}")).concat_space(if fields.is_empty() {
                D::nil().braces()
            } else {
                D::space()
                    .concat(D::intersperse(
                        fields.iter().zip(args.iter()).map(|((name, _ty), e)| {
                            D::text(name.as_str())
                                .concat(D::text(":"))
                                .concat_space(exp(context, e))
                        }),
                        D::text(",").concat(D::space()),
                    ))
                    .concat(D::space())
                    .braces()
            })
        }

        DataOp::Pack(struct_) => {
            let fields = &struct_.struct_.fields.0;
            assert!(fields.len() == args.len());
            let struct_name = struct_.struct_.name;
            D::text(format!("{struct_name}")).concat_space(if fields.is_empty() {
                D::nil().braces()
            } else {
                D::space()
                    .concat(D::intersperse(
                        fields.iter().zip(args.iter()).map(|((name, _ty), e)| {
                            D::text(name.as_str())
                                .concat(D::text(":"))
                                .concat_space(exp(context, e))
                        }),
                        D::text(",").concat(D::space()),
                    ))
                    .concat(D::space())
                    .braces()
            })
        }

        DataOp::Unpack(_) => unreachable!("Unpack"),
        DataOp::VecUnpack(_) => unreachable!("VecUnpack"),

        DataOp::UnpackVariant(_)
        | DataOp::UnpackVariantImmRef(_)
        | DataOp::UnpackVariantMutRef(_) => unreachable!("Unpack variant"),
    }
}

fn primitive_op_doc(context: &Context, op: &PrimitiveOp, args: &[Exp]) -> Doc {
    let bin = |lhs: &Exp, sym: &str, rhs: &Exp| {
        exp(context, lhs)
            .concat_space(D::text(sym.to_string()))
            .concat_space(exp(context, rhs))
    };

    match op {
        PrimitiveOp::CastU8 => exp(context, &args[0]).concat(D::text("as u8")),
        PrimitiveOp::CastU16 => exp(context, &args[0]).concat(D::text("as u16")),
        PrimitiveOp::CastU32 => exp(context, &args[0]).concat(D::text("as u32")),
        PrimitiveOp::CastU64 => exp(context, &args[0]).concat(D::text("as u64")),
        PrimitiveOp::CastU128 => exp(context, &args[0]).concat(D::text("as u128")),
        PrimitiveOp::CastU256 => exp(context, &args[0]).concat(D::text("as u256")),

        PrimitiveOp::Add => bin(&args[0], "+", &args[1]),
        PrimitiveOp::Subtract => bin(&args[0], "-", &args[1]),
        PrimitiveOp::Multiply => bin(&args[0], "*", &args[1]),
        PrimitiveOp::Modulo => bin(&args[0], "%", &args[1]),
        PrimitiveOp::Divide => bin(&args[0], "/", &args[1]),
        PrimitiveOp::BitOr => bin(&args[0], "|", &args[1]),
        PrimitiveOp::BitAnd => bin(&args[0], "&", &args[1]),
        PrimitiveOp::Xor => bin(&args[0], "^", &args[1]),
        PrimitiveOp::Or => bin(&args[0], "||", &args[1]),
        PrimitiveOp::And => bin(&args[0], "&&", &args[1]),
        PrimitiveOp::Equal => bin(&args[0], "==", &args[1]),
        PrimitiveOp::NotEqual => bin(&args[0], "!=", &args[1]),
        PrimitiveOp::LessThan => bin(&args[0], "<", &args[1]),
        PrimitiveOp::GreaterThan => bin(&args[0], ">", &args[1]),
        PrimitiveOp::LessThanOrEqual => bin(&args[0], "<=", &args[1]),
        PrimitiveOp::GreaterThanOrEqual => bin(&args[0], ">=", &args[1]),

        PrimitiveOp::Not => D::text("!").concat(exp(context, &args[0]).parens()),

        PrimitiveOp::ShiftLeft => bin(&args[0], "<<", &args[1]),
        PrimitiveOp::ShiftRight => bin(&args[0], ">>", &args[1]),
    }
}

fn value(v: &Value) -> Doc {
    match v {
        Value::Bool(b) => D::text(b.to_string()),
        Value::U8(u) => D::text(u.to_string()).concat(D::text("u8")),
        Value::U16(u) => D::text(u.to_string()).concat(D::text("u16")),
        Value::U32(u) => D::text(u.to_string()).concat(D::text("u32")),
        Value::U64(u) => D::text(u.to_string()).concat(D::text("u64")),
        Value::U128(u) => D::text(u.to_string()).concat(D::text("u128")),
        Value::U256(u) => D::text(u.to_string()).concat(D::text("u256")),
        Value::Address(a) => D::text(format!("@{:X}", a)),
        Value::Vector(values) => D::text("vector[")
            .concat(D::intersperse(
                values.iter().map(value),
                D::text(",").concat(D::space()),
            ))
            .concat(D::text("]")),
        Value::Struct(_) | Value::Signer(_) | Value::Variant(_) => unreachable!(),
    }
}
