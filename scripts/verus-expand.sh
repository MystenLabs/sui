# Produces the "plain code" artifact for each verified crate: the crate's
# source with the `verus!` proc macro evaluated, which strips out all proof and
# spec code and leaves only the executable Rust. The artifact is checked in (so
# reviewers can read exactly what runs) but never compiled.
#
# The `verus!` macro is just a proc macro; evaluating it is what `rustc` does
# during a normal build. We capture that by asking rustc to emit the expanded
# crate (-Zunpretty=expanded), then drop the compiler-injected prelude and run
# rustfmt so the result is stable and readable.
#
# Usage:
#   scripts/verus-expand.sh           regenerate and write the artifacts
#   scripts/verus-expand.sh --check   fail if any checked-in artifact is stale
#
# No Verus toolchain is required — this uses the workspace's stable rustc.

set -euo pipefail

# "<crate name>|<package directory>" entries (dir relative to repo root). The
# artifact is written to <dir>/expanded/<crate>.rs. Plain indexed array (not
# associative) so this runs on macOS's bash 3.2. Keep in sync with verus-check.sh.
CRATES=(
    "sui-types-verified|crates/sui-types/verified"
)

MODE="write"
if [[ "${1:-}" == "--check" ]]; then
    MODE="check"
elif [[ -n "${1:-}" ]]; then
    echo "ERROR: unknown argument '${1}' (expected --check or nothing)" >&2
    exit 1
fi

REPO_ROOT="$(git rev-parse --show-toplevel)"
cd "${REPO_ROOT}"

# Emit the expanded, deprelude'd, formatted plain code for a crate to stdout.
expand_crate() {
    local crate="$1"
    # RUSTC_BOOTSTRAP=1 lets the stable toolchain accept the unstable
    # -Zunpretty flag. The grep strips rustc's injected `std` prelude preamble,
    # which is not part of the crate's own source.
    RUSTC_BOOTSTRAP=1 cargo rustc -q -p "${crate}" --lib -- -Zunpretty=expanded \
        | grep -vxE '#!\[feature\(prelude_import\)\]|#\[prelude_import\]|use std::prelude::[a-z0-9_]+::\*;|#\[macro_use\]|extern crate std;' \
        | rustfmt --edition 2024 \
        | awk '
            # 1. Restore doc comments. -Zunpretty desugars `///` to
            #    `#[doc = "..."]`; rustfmt has collapsed each onto one line, so we
            #    can convert them back. Unescaping is confined to these lines (\47
            #    is an apostrophe, avoiding shell-quote pain) so string literals in
            #    exec code are left untouched. Module-level `//!` docs survive
            #    expansion as-is and need no handling.
            /^[ \t]*#\[doc = ".*"\][ \t]*$/ {
                indent = $0; sub(/#\[doc = ".*/, "", indent)
                content = $0
                sub(/^[ \t]*#\[doc = "/, "", content)
                sub(/"\][ \t]*$/, "", content)
                gsub(/\\\47/, "\47", content)   # backslash-apostrophe -> apostrophe
                gsub(/\\"/, "\"", content)       # backslash-quote -> quote
                gsub(/\\\\/, "\\", content)      # double-backslash -> backslash
                print indent "///" content
                next
            }
            # 2. Insert the generated banner right after the SPDX line so the
            #    license stays first (the repo license check requires it on top).
            { print }
            !banner && /SPDX-License-Identifier/ {
                print ""
                print "// @generated by scripts/verus-expand.sh — DO NOT EDIT."
                print "// Plain Rust with the verus! macro evaluated (all proof and spec code"
                print "// stripped). Checked in for review only; this file is never compiled."
                banner = 1
            }
        '
}

status=0
for entry in "${CRATES[@]}"; do
    crate="${entry%%|*}"
    dir="${entry#*|}"
    out="${dir}/expanded/${crate}.rs"
    echo ">> expanding ${crate} -> ${out}"
    tmp="$(mktemp)"
    expand_crate "${crate}" > "${tmp}"

    if [[ "${MODE}" == "check" ]]; then
        if ! diff -u "${out}" "${tmp}" 2>/dev/null; then
            echo "ERROR: ${out} is stale. Run scripts/verus-expand.sh and commit." >&2
            status=1
        fi
        rm -f "${tmp}"
    else
        mkdir -p "${dir}/expanded"
        mv "${tmp}" "${out}"
    fi
done

if [[ "${MODE}" == "check" && "${status}" -eq 0 ]]; then
    echo ">> all expanded artifacts up to date"
fi
exit "${status}"
